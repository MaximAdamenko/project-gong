import cv2, json, glob, numpy as np
import mediapipe as mp
from mediapipe.tasks import python
from mediapipe.tasks.python import vision

STD_THRESHOLD = 0.035        # שונות MAR מעל זה => דיבר (לכייל לפי הדירוג)
MODEL = "/content/face_landmarker.task"
UPPER, LOWER, LEFT, RIGHT = 13, 14, 61, 291

def mouth_aspect_ratio(lm, w, h):
    pt = lambda i: np.array([lm[i].x * w, lm[i].y * h])
    return np.linalg.norm(pt(UPPER) - pt(LOWER)) / (np.linalg.norm(pt(LEFT) - pt(RIGHT)) + 1e-6)

def make_landmarker():
    opts = vision.FaceLandmarkerOptions(
        base_options=python.BaseOptions(model_asset_path=MODEL),
        num_faces=1, running_mode=vision.RunningMode.IMAGE)
    return vision.FaceLandmarker.create_from_options(opts)

def load_face_boxes(json_path):
    """frame_index -> [(face_id, x,y,w,h normalized)] מתוך הפלט של צוות הפנים."""
    data = json.load(open(json_path))
    by_frame = {}
    for fr in data["frames"]:
        by_frame[fr["frame_index"]] = [
            (f["face_id"], f["bounding_box"]["x_min"], f["bounding_box"]["y_min"],
             f["bounding_box"]["width"], f["bounding_box"]["height"])
            for f in fr["faces_detected"]]
    return by_frame, data.get("total_participants_detected")

def analyze(video_path, boxes_by_frame, frame_skip=5, pad=0.30):
    cap = cv2.VideoCapture(video_path)
    total = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
    lm = make_landmarker()
    series, idx = {}, 0
    while True:
        ok, frame = cap.read()
        if not ok:
            break
        if idx % frame_skip == 0 and idx in boxes_by_frame:
            if idx % 2000 == 0:
                print(f"  מעבד... {idx}/{total}")
            H, W = frame.shape[:2]
            for fid, xn, yn, wn, hn in boxes_by_frame[idx]:
                x, y, w, h = xn * W, yn * H, wn * W, hn * H      # נורמלי -> פיקסלים
                px, py = w * pad, h * pad                         # ריווח סביב הפרצוף
                crop = frame[max(0, int(y - py)):min(H, int(y + h + py)),
                             max(0, int(x - px)):min(W, int(x + w + px))]
                if crop.size == 0:
                    continue
                rgb = cv2.cvtColor(crop, cv2.COLOR_BGR2RGB)
                res = lm.detect(mp.Image(image_format=mp.ImageFormat.SRGB, data=rgb))
                if res.face_landmarks:
                    ch, cw = crop.shape[:2]
                    series.setdefault(fid, []).append(
                        mouth_aspect_ratio(res.face_landmarks[0], cw, ch))
        idx += 1
    cap.release()
    return series

