import cv2, json, glob, numpy as np
import mediapipe as mp
from mediapipe.tasks import python
from mediapipe.tasks.python import vision

# ---------------- פרמטרים לכיול ----------------
SPEECH_THRESHOLD = 300.0      # אנרגיית-דיבור מעל זה => דיבר (כוונן לפי הדירוג שמודפס)
FRAME_SKIP       = 3          # 3 = איזון טוב | 2 = מדויק/איטי | 4 = מהיר
SPEECH_LO, SPEECH_HI = 1.5, 6.0   # תחום תדרי דיבור (הרץ)
MODEL = "/content/face_landmarker.task"
UPPER, LOWER, LEFT, RIGHT = 13, 14, 61, 291   # נקודות שפתיים (Face Landmarker)
# ------------------------------------------------


def mouth_aspect_ratio(lm, w, h):
    """יחס פתיחת הפה: מרחק אנכי בין השפתיים חלקי רוחב הפה (מנורמל למרחק)."""
    pt = lambda i: np.array([lm[i].x * w, lm[i].y * h])
    return np.linalg.norm(pt(UPPER) - pt(LOWER)) / (np.linalg.norm(pt(LEFT) - pt(RIGHT)) + 1e-6)


def speech_power(sig, fs):
    """אנרגיה מוחלטת בתחום תדרי הדיבור. דיבור=תנודה מהירה->גבוה;
    חיוך/ניד-ראש=שינוי איטי->מתחת לתחום; שתיקה=רעש חלש->נמוך."""
    sig = np.asarray(sig, float)
    if len(sig) < 32:
        return 0.0
    sig = sig - sig.mean()
    freqs = np.fft.rfftfreq(len(sig), 1 / fs)
    power = np.abs(np.fft.rfft(sig)) ** 2
    hi = min(SPEECH_HI, fs / 2 * 0.95)          # לא לעבור את תדר נייקוויסט
    return float(power[(freqs >= SPEECH_LO) & (freqs <= hi)].sum() / len(sig) * 1000)


def make_landmarker():
    opts = vision.FaceLandmarkerOptions(
        base_options=python.BaseOptions(model_asset_path=MODEL),
        num_faces=1, running_mode=vision.RunningMode.IMAGE)
    return vision.FaceLandmarker.create_from_options(opts)


def load_face_boxes(json_path):
    """טוען את פלט צוות הפנים: frame_index -> [(face_id, x,y,w,h מנורמל)]."""
    data = json.load(open(json_path))
    by_frame = {fr["frame_index"]: [
        (f["face_id"], f["bounding_box"]["x_min"], f["bounding_box"]["y_min"],
         f["bounding_box"]["width"], f["bounding_box"]["height"])
        for f in fr["faces_detected"]] for fr in data["frames"]}
    return by_frame, data.get("total_participants_detected"), data.get("fps", 30)


def measure_mar(video, boxes_by_frame, pad=0.30):
    """מחזיר {face_id: [סדרת MAR לאורך הזמן]} — שלב הניתוח הכבד."""
    cap = cv2.VideoCapture(video)
    total = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
    lm = make_landmarker()
    series, idx = {}, 0
    while True:
        ok, frame = cap.read()
        if not ok:
            break
        if idx % FRAME_SKIP == 0 and idx in boxes_by_frame:
            if idx % 3000 == 0:
                print(f"  מעבד... {idx}/{total}")
            H, W = frame.shape[:2]
            for fid, xn, yn, wn, hn in boxes_by_frame[idx]:
                x, y, w, h = xn * W, yn * H, wn * W, hn * H
                px, py = w * pad, h * pad           # ריווח סביב התיבה לזיהוי טוב יותר
                crop = frame[max(0, int(y - py)):min(H, int(y + h + py)),
                             max(0, int(x - px)):min(W, int(x + w + px))]
                if crop.size == 0:
                    continue
                res = lm.detect(mp.Image(image_format=mp.ImageFormat.SRGB,
                                         data=cv2.cvtColor(crop, cv2.COLOR_BGR2RGB)))
                if res.face_landmarks:
                    ch, cw = crop.shape[:2]
                    series.setdefault(fid, []).append(
                        mouth_aspect_ratio(res.face_landmarks[0], cw, ch))
        idx += 1
    cap.release()
    return series


def report(series, fs, total_participants, threshold=SPEECH_THRESHOLD):
    """שלב הכיול — מהיר. אפשר לקרוא שוב עם סף אחר בלי לנתח מחדש."""
    scored = sorted(((fid, speech_power(s, fs)) for fid, s in series.items()),
                    key=lambda kv: -kv[1])
    print(f"\n{'face_id':<10}{'אנרגיית-דיבור':<16}{'מדבר?'}")
    speaking = 0
    for fid, sp in scored:
        is_sp = sp > threshold
        speaking += is_sp
        print(f"{fid:<10}{round(sp, 1):<16}{'✅ מדבר' if is_sp else '— שותק'}")
    print(f"\nמשתתפים בסך הכל (מצוות הפנים): {total_participants}")
    print(f"מדברים (לפי תנועת שפתיים):     {speaking}")
    return speaking


