print(f"קצב דגימה: {fs:.1f}Hz | תחום דיבור: {SPEECH_LO}-{min(SPEECH_HI, fs/2*0.95):.1f}Hz")

series = measure_mar(VIDEO, boxes_by_frame)   # ניתוח כבד (פעם אחת)
report(series, fs, total_participants);         # דוח + כיול

