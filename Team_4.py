print(f"\n{'face_id':<10}{'std':<10}{'frames':<9}{'מדבר?'}")
speaking = 0
for fid in sorted(series):
    std = float(np.std(series[fid]))
    sp = std > STD_THRESHOLD
    speaking += sp
    print(f"{fid:<10}{round(std,4):<10}{len(series[fid]):<9}{'✅ מדבר' if sp else '— שותק'}")

print(f"\nמשתתפים בסך הכל (מצוות הפנים): {total_participants}")
print(f"מדברים (לפי תנועת שפתיים):     {speaking}")








